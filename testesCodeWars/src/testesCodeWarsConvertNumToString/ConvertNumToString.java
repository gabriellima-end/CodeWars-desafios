package testesCodeWarsConvertNumToString;

//CONVERTA INT PARA STRING

public class ConvertNumToString {
	
	public static void main(String[] args) {
		
		int num = 10;
		
		System.out.println(numberToString(num));
		
	}
	
	public static String numberToString(int num) {
		
		
		String convertNum = Integer.toString(num);
		
		return convertNum;
	}
	
	
}

